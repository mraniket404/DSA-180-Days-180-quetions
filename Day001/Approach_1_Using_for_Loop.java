// Day 1 - Print Numbers from 1 to N 
// Question 
// Ek number N diya gaya hai. 1 se N tak ke numbers print karo. 

public class Approach_1_Using_for_Loop {
   
    public static void main(String[] args) {
            int n = 10 ;
            for (int i =1 ; i<=n;i++){
                System.out.println(i);
            }
    }           
}